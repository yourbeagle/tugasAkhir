    public void Edit()
    {
        String sql = "select * from pendaftaran where idwisuda=?";
        try {
            con = config.configDB();
            pst = con.prepareStatement(sql);
            pst.setString(1, idwisuda);
            rs = pst.executeQuery();
            if(rs.next())
            {
                btnEdit.setEnabled(true);
                btnSubmit.setEnabled(false);
            }
            else
            {
                btnEdit.setEnabled(false);
                btnSubmit.setEnabled(true);
            }
        } catch (Exception e) {
        }
    }
    
    public void load_table()
    {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID Wisuda");
        model.addColumn("NRP");
        model.addColumn("Nama Mahasiswa");
        model.addColumn("Prodi");
        model.addColumn("Dosen");
        model.addColumn("Nama Dosen");
        model.addColumn("IPK");
        model.addColumn("Judul");
        try {
            int no = 1;
            String sql = "select * from pendaftaran";
            con = (Connection) config.configDB();
            stm = con.createStatement();
            rs = stm.executeQuery(sql);
            
            while(rs.next())
            {
                model.addRow(
                new Object[]
                {
                    no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8)
                }
                );
            }
            tbEdit.setModel(model);
            tbHapus.setModel(model);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
